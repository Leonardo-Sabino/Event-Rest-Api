class User {
  constructor(
    id,
    username,
    email,
    password,
    token,
    userimage,
    gender,
    tokenDevice,
    role
  ) {
    this.id = id;
    this.username = username;
    this.email = email;
    this.password = password;
    this.token = token;
    this.userimage = userimage;
    this.gender = gender;
    this.tokenDevice = tokenDevice;
    this.role = role;
  }
}
